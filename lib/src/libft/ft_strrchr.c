#include "../../include/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char			*last;
	unsigned char	find;
	size_t			i;

	last = (char *)s;
	find = (unsigned char)c;
	i = ft_strlen(s);
	while (i > 0)
	{
		if (last[i] == find)
			return (last + i);
		i--;
	}
	if (last[i] == find)
		return (last);
	return (0);
}
